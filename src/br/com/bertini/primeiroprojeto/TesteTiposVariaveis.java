package br.com.bertini.primeiroprojeto;

public class TesteTiposVariaveis {

	
	public static void main(String[] args) {

		//Primitivos
		int idadePrimitivo = 0;
		double valorPrimitivo = 10.70;
		boolean boleanoPrimitivo = false;
		
		System.out.println(idadePrimitivo);
		System.out.println(valorPrimitivo);
		System.out.println(boleanoPrimitivo);
		
		//Wrappers
		Integer idadeWrapper = 0;
		Double valorWrapper  = 10.70;
		Boolean boleanoWrapper  = false;
		
		System.out.println(idadeWrapper);
		System.out.println(valorWrapper);
		System.out.println(boleanoWrapper);
	}
}
